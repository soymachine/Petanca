// vista "ABUELO_DATA" de un país: solo price/stats/trait/clima, sin
// nombre/descripción (eso vive en facesFor, igual que ABUELO_DATA/FACES
// están separados para España)
export function abueloDataFor(country) {
  return FOUNDERS[country].map(({ price, stats, trait, clima }) => ({ price, stats, trait, clima }));
}

// vista "FACES" de un país: nombre/descripción + retrato asignado de
// forma determinista y ESTABLE (mismo país+índice siempre da la misma
// foto, para que recargar la partida no le cambie la cara a nadie)
export function facesFor(country) {
  return FOUNDERS[country].map((f, i) => {
    const entry = RIVAL_PHOTO_POOL[Math.abs(hashStr(`founder-${country}-${i}-${f.name}`)) % RIVAL_PHOTO_POOL.length];
    return { name: f.name, desc: f.desc, photo: entry.photo, mini: entry.mini };
  });
}
